public class PointTracker {
    private double credit;
    private double total;
    private int numberOfTries;

    public PointTracker() {
        this.total = total + credit;
        this.credit = QuestionGenerator.getDifficulty() / numberOfTries;
        this.numberOfTries = 1;
    }
    
    
}
